    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for (int i = 0; i < filmes.size(); i++) {
            System.out.println("_________________________________________________________________________________");
            System.out.println("|Dados do " + (i+1) + "º filme:");
            System.out.println("|    Nome: " + filmes.get(i).nome);
            System.out.println("|    Ano: " + filmes.get(i).ano);
            System.out.println("|    Gênero: " + filmes.get(i).genero);
            System.out.println("|    Protagonista: " + filmes.get(i).protagonista);
            System.out.println("_________________________________________________________________________________");
        }
        System.out.println("_______________________________FIM DA LISTAGEM_______________________________");
    }//GEN-LAST:event_jButton1ActionPerformed

